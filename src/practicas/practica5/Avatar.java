package practicas.practica5;

public class Avatar {

	private String name;
	private int vida;
	public Avatar() {}
	public Avatar(String name, int vida) {
		this.name = name;
		this.vida = vida;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getVida() {
		return vida;
	}
	public void setVida(int vida) {
		this.vida = vida;
	}
	
	
}

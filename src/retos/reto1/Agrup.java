package retos.reto1;

public class Agrup {
	
	private int s;
	private int g;
	
	public Agrup(int s, int g) {
		this.s = s;
		this.g = g;
		
	}

	public int getS() {
		return s;
	}

	public void setS(int s) {
		this.s = s;
	}

	public int getG() {
		return g;
	}

	public void setG(int g) {
		this.g = g;
	}
	
	
}

public class math {
	
	/**
	 * Pre:
	 * Post: Metodo suma
	 */
	public static int suma(int a, int b) {
		
		return a+b;
	}
	
	/**
	 * Pre:
	 * Post: Metodo resta
	 */
	public static int resta(int a, int b) {
		
		return a-b;
	}

}
